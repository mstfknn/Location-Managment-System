
public class PlaceType {
	private int ID;
	private String name;
	private String color;
	public PlaceType( String name, String color) {

		ID = ID;
		this.name = name;
		this.color = color;
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}

}
